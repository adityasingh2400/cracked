// Creative & Audience family — evaluation baseline rubric.
//
// ── Tier ladder (what each band means) ──────────────────────────────────────
//
//   D — creator-curious. Identifies as writer/designer/artist with no portfolio,
//       audience, publication, or paid creative output. Courses and aspiration only.
//
//   C — building, pre-breakout. 1K+ audience, student work, spec projects, zines,
//       open mics, side gigs, submissions in flight, art/design/film school enrolled.
//
//   B — professional output with traction. 10K+ audience, monetized channel,
//       portfolio with clients, DTC revenue, completed short film, agency intern,
//       Etsy/print sales, regular paid gigs, self-published with sales.
//
//   A — breakout signal. 50K–100K+ audience, reputable publisher, gallery rep,
//       festival win, major brand collab, charting podcast, MFA from top program,
//       sold-out run, Webby/Cannes Lions award, staff role at known agency/studio.
//
//   S — top 1%, obviously cracked. 1M+ audience, Big-5 author, NYT bestseller,
//       design lead at Apple/Figma, CD at W+K, Cannes/SXSW selection, Art Basel,
//       streaming series credit, Grammy nom, Vogue feature, RISD + shipped work.
//
//   MYTHIC — career-defining (0.1%). 10M+ audience, AIGA Medal, Pentagram partner,
//       New Yorker staff writer, Oscar/Emmy nom, Palme/Cannes feature, $100M brand,
//       luxury CD (LV/Nike), National Book Award, YouTube Diamond.
//
//   ASCENDED — monumental cultural impact (0.001%). Pulitzer/Nobel/Booker/Pritzker,
//       Oscar/Emmy/Grammy win, 100M+ audience, $1B+ consumer brand, MoMA/Tate
//       retrospective, defined an era of taste (Virgil/Kaws-class).
//
// ── Crown math (A/S only) — src/lib/tier-crowns.ts ──────────────────────────
//
//   A crown  = 1 matched A achievement  OR  4 matched B achievements.
//   S crown  = 1 matched S achievement  OR  4 matched A achievements.

