// Founder family — evaluation baseline rubric.
//
// ── Tier ladder (what each band means) ──────────────────────────────────────
//
//   D — founder-curious. No incorporated entity, no live product, no traction.
//       Headline-only founder, idea stage, wantrepreneur language.
//
//   C — actively building, pre-revenue or pre-launch. Stealth, student incubator,
//       first attempt, building in public, part-time nights-and-weekends.
//
//   B — shipped something real. MVP live, side revenue, 1K+ users, Product Hunt
//       launch, co-founder at seed-stage, bootstrapped $1K+ MRR, ex-FAANG founding.
//
//   A — top 5–10% early institutional signal. Pre-seed/angel from named investors,
//       $10K+ MRR indie SaaS, hackathon→funded startup, acqui-hire, accelerator
//       grad, SBIR grant, $100K crowdfunding, registered company + founder title.
//
//   S — top 1%, obviously cracked. YC or top accelerator, Series A, $1M+ seed,
//       $10M+ ARR, $10M+ exit, 100K+ users, Forbes 30U30, Product Hunt #1.
//
//   MYTHIC — career-defining (0.1%). Unicorn ($1B+ valuation), IPO, $100M+ exit,
//       Thiel Fellow who scaled, Series B/C at scale, $100M+ ARR, repeat exit.
//
//   ASCENDED — monumental impact beyond unicorn (0.001%). Decacorn ($10B+),
//       centicorn ($100B+), repeat unicorn founder, category-defining platform,
//       100M–1B+ users, Nobel-class founder-scientist (Hassabis), field-defining
//       founder-educator at civilization scale (Karpathy-class arc).
//
// ── Crown math (A/S only) — src/lib/tier-crowns.ts ──────────────────────────
//
//   A crown  = 1 matched A achievement  OR  4 matched B achievements.
//   S crown  = 1 matched S achievement  OR  4 matched A achievements.
//
//   Examples at S tier:
//     • 3 S achievements                          → S3
//     • 1 S + 8 A achievements                    → S3
//     • 12 A achievements (no S; tier bumped via chain) → S3
//
//   Examples at A tier:
//     • 2 A + 4 B                                 → A3
//     • 12 B (no A)                               → A3

