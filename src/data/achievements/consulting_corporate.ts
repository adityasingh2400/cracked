function mbb(title?: string[]): SignalMatcher[] {
  return [{ kind: "company", match: MBB, ...(title ? { title } : {}) }];
}

function cxo(titles: string[]): SignalMatcher[] {
  return [{ kind: "company", match: [""], title: titles }];
}

function f500(...extra: SignalMatcher[]): SignalMatcher[] {
  return [{ kind: "free_text", patterns: [/Fortune\s?(500|100|50|10)|F500|Global\s?500/i] }, ...extra];
}

